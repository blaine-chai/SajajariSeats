package com.example.sajajariseats.datasets;

public class RoomTotalData {
	private int seatNumMax;
	private int filled;
	private int empty;
	private double percent;

	public int getSeatNumMax() {
		return seatNumMax;
	}

	public void setSeatNumMax(int seatNumMax) {
		this.seatNumMax = seatNumMax;
	}

	public int getFilled() {
		return filled;
	}

	public void setFilled(int filled) {
		this.filled = filled;
	}

	public int getEmpty() {
		return empty;
	}

	public void setEmpty(int empty) {
		this.empty = empty;
	}

	public double getPercent() {
		return percent;
	}

	public void setPercent(double percent) {
		this.percent = percent;
	}

}
